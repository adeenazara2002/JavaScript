document.write("<h2>Result:</h2><br>")
let a = 10
document.write("The value of a is "+ a +"<br>")
document.write("----------------------"+"<br>")

a = ++a
document.write("The value of ++a is "+ a +"<br>")
document.write("Now the value of a is "+ a +"<br>")
document.write("----------------------"+"<br>")

a++
document.write("The value of a++ is "+ a +"<br>")
document.write("Now the value of a is "+ a +"<br>")
document.write("----------------------"+"<br>")

a = --a
document.write("The value of --a is "+ a +"<br>")
document.write("Now the value of a is "+ a +"<br>")
document.write("----------------------"+"<br>")

a--
document.write("The value of a-- is "+ a +"<br>")
document.write("Now the value of a is "+ a +"<br>")



